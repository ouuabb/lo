    console.log(chalk.cyan('\n    lo push <remote>'));
    console.log(`
    作用：将本地产生的变更推送到远程。

    执行流程：
      1. 先执行 lo sync（扫描文件系统，更新 DB，生成未同步的 ops）
      2. 读取锚点，获取自上次推送以来的新操作
      3. 收集这些操作关联的资源文件，打包成 batch
      4. SCP 传输 batch 到远程（或复制到本地路径）
      5. 更新本地锚点

    返回信息：
      Pushed N ops to <remote>
      Batch: batch_1749200000123

    什么情况下使用：
      - 你写好了一批笔记，想把变更同步到其他设备
      - 使用频率：建议每天结束工作前执行一次
      - 不用担心重复推送——幂等性保证不会出问题

    示例：
      lo push user@my-server:/home/notes          # 推送到远程服务器
      lo push /mnt/usb/notes-backup               # 推送到 U 盘/移动硬盘`);

    console.log(chalk.cyan('\n    lo pull <remote>'));
    console.log(`
    作用：从远程拉取最新变更到本地。

    执行流程：
      1. 下载远程最新的 batch（确定文件名最大的那个）
      2. 解包，校验 SHA-256（任何不匹配 → 丢弃整个批次）
      3. 安装资源文件到本地 resources/
      4. 逐条应用 ops 到本地 SQLite（冲突检测在此步骤）
      5. 执行 lo sync（重新索引新文件）
      6. 更新锚点

    返回信息：
      Pulled N files, applied M ops, K conflicts

    什么情况下使用：
      - 你在另一台设备上编辑了笔记，回到这台设备想获取最新版本
      - 使用频率：建议开始工作前执行一次
      - 首次使用：如果之前从未同步过，应该用 lo clone 而不是 lo pull

    示例：
      lo pull user@my-server:/home/notes          # 从远程服务器拉取`);

    console.log(chalk.cyan('\n    lo clone <remote>'));
    console.log(`
    作用：从远程完整克隆一个笔记仓库（类似 git clone）。

    执行流程：
      1. 下载远程 sync_batches/ 下所有 batch 文件
      2. 按文件名排序（时间顺序），逐个解包校验
      3. 合并所有 batch 的 ops，按 op_id 去重，按 timestamp 排序
      4. 创建新的空仓库（lo init）
      5. 安装所有资源文件到本地
      6. 从头重放所有 ops（按时间顺序），重建完整的 DB 状态
      7. 设置锚点为最后一个 batch 的最后一条 op

    与 pull 的区别：
      pull 只下载"最新的一个 batch"，clone 下载"所有 batch"
      pull 假设你已经有一个仓库在运行，clone 从零开始
      clone 之后你就可以用 push/pull 正常增量同步了

    什么情况下使用：
      - 在新设备上搭建笔记环境
      - 第一次从远程获取仓库
      - 本地仓库损坏，需要从远程重建

    示例：
      lo clone user@my-server:/home/notes --dest ./my-notes
      cd my-notes
      lo auth add -k ~/.ssh/id_ed25519   # 绑定 SSH 密钥以解密`);

    console.log(chalk.cyan('\n    lo sync'));
    console.log(`
    作用：扫描本地文件系统，将变更同步到 SQLite 数据库。
          （这是本地操作，不涉及远程传输）

    执行流程：
      1. 扫描 resources/ 下所有文件
      2. 对每个文件：
         - 路径在 DB 中且已跟踪 → 检查 hash 是否变化 → resource_updated
         - 路径不在 DB 中 → 标记为候选新文件
      3. 第二阶段：匹配 "从 DB 消失的路径" 和 "候选新文件"：
         - hash 相同 → 重命名检测 → resource_moved
         - hash 不同 → 真删除 → resource_deleted
      4. 剩下的候选新文件 → resource_created

    什么情况下使用：
      - 你通过外部编辑器（VS Code 等）直接编辑了 .md 文件
      - 你手动在 resources/ 目录下添加/删除/重命名了文件
      - lo push 会自动先执行 lo sync，所以通常不需要手动执行
      - 但如果只做本地管理不推送到远程，lo sync 是必须的`);

