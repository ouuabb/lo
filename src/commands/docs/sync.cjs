    console.log(`
  锚点解决了"哪些操作已经同步过了"这个问题。

  存储位置：sync_config 表
  存储格式：sync.anchor.<remoteId> = { last_op_id, last_op_timestamp }

    lo push 时：
      1. 读取本地与 <remoteId> 的锚点
      2. 查询 sync_ops 中 timestamp > last_op_timestamp 的新操作
      3. 将这些新操作打成 batch 推送
      4. 成功后更新锚点

    lo pull 时：
      1. 下载远程最新的 batch
      2. 解包、校验、应用 ops
      3. 将 batch 的 last_op_id/last_op_timestamp 存为锚点
      4. 下次 pull 时以此为起点，只下载更新的 batch

  极端情况——首次同步：
    锚点为空 → push 发送所有本设备产生的操作（相当于"全量"）
    这在首次多设备连接时自动发生

  锚点是按 (设备, 远程) 独立维护的：
    设备 A 对 远程 X 的锚点 ≠ 设备 B 对 远程 X 的锚点
    互不影响`);

